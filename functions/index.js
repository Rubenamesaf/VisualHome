const functions = require("firebase-functions");
const nodemailer = require("nodemailer");

const gmailEmail = "rubenamesaf@gmail.com";
const gmailPassword = "Mediaisla2023";

const transporter = nodemailer.createTransport({
  service: "gmail",
  auth: {
    user: gmailEmail,
    pass: gmailPassword,
  },
});

exports.sendEmail = functions.https.onRequest((req, res) => {
  const dest = req.query.dest;

  const mailOptions = {
    from: gmailEmail,
    to: dest,
    subject: "Correo de prueba desde Firebase",
    text: "Este es un correo de prueba enviado desde una funciÃ³n de Firebase.",
  };

  transporter.sendMail(mailOptions, (error, info) => {
    if (error) {
      return res.status(500).send(error.toString());
    }
    return res.status(200).send("Correo enviado: " + info.response);
  });
});